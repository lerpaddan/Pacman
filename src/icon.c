void icon_draw(Icon const *icon, Vector const *vector)
{
	for(int y = 0; y < icon->size.y; ++y)
	{
		for(int x = 0; x < icon->size.x; ++x)
		{
			graphics_set(vector->x + x, vector->y + y, icon->data[y * icon->size.x + x]);
		}
	}
}


